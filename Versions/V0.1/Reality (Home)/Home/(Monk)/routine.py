class Me:
    def __init__(self, name):
        self.name = name

    def breathe(self):
        print(f"{self.name} is breathing.")

    def look(self):
        print(f"{self.name} is looking.")

    def think(self):
        print(f"{self.name} is thinking.")

    def feel(self):
        print(f"{self.name} is feeling.")

    def talk(self):
        print(f"{self.name} is talking.")

    def flow(self):
        print(f"{self.name} is flowing.")

    def vibe(self):
        print(f"{self.name} is vibing.")

    def be(self):
        print(f"{self.name} is being.")

    def do(self):
        print(f"{self.name} is doing.")

